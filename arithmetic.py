a = int(input("Enter no 1: "))
b = int(input("Enter no 2: "))

operator = input("Enter any operator: ")

if operator == "+":
    print("Addition:", a + b)
elif operator == "-":
    print("Subtraction:", a - b)
elif operator == "/":
    print("Division:", a / b)
elif operator == "*":
    print("Multiplication:", a * b)
else:
    print("Invalid operator.")

    
    
    
    
