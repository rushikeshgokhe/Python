chr=input("Enter any colour code:")
if chr=="r"  or chr=="R":
    print("Red")
elif chr=="w" or chr=="W":
    print("white")
elif chr=="y" or chr=="Y":
    print("yellow")
elif chr=="b" or chr=="B":
    print("Blue")
elif chr=="g" or chr=="G":
    print("Green")
else:
    print("Invalid charecter")
                
